/**
 * Test function
 *
 * @param x Value to double
 * @returns The doubled value
 */
export function double(x: number): number {
  return x * 2;
}

/**
 * Test function
 *
 * @param x Value to triple
 * @returns The tripled value
 * @version 1.1.0
 */
export function triple(x: number): number {
  return x * 3;
}
